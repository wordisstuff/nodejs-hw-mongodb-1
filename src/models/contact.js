import { model, Schema } from 'mongoose';
const contactSchema = new Schema(
  {
    name: {
      type: String,
      required: true,
    },
    phoneNumber: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: false,
    },
    isFavourite: {
      type: Boolean,
      required: true,
      default: false,
    },
    contactType: {
      type: String,
      required: true,
      enum: ['work', 'home', 'personal'],
      default: 'personal',
    },
    createdAt:{Date},
    updatedAt :{Date},
  },
  {
    timestamps: true,
  },
);
export const ContactCollection = model('contacts', contactSchema);